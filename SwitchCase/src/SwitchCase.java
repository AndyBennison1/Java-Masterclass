public class SwitchCase {


    public static void main(String[] args) {
    char switchcase1 = 'B';

    switch(switchcase1) {
        case 'A':
            System.out.println("Letter " + switchcase1 + " found!");
            break;

        case 'B':
            System.out.println("Letter " + switchcase1 + " found!");
            break;

        case 'C':
            System.out.println("Letter " + switchcase1 + " found!");
            break;

            case 'D':
            System.out.println("Letter " + switchcase1 + " found!");
            break;

            case 'E':
            System.out.println("Letter " + switchcase1 + " found!");
            break;

        default:
            System.out.println("No valid letter found!");


    }



    }
}
